		<p><strong>제 7조 개인정보의 파기 절차 및 파기방법 </strong></p>
		<p>본원은 원칙적으로 개인정보 수집 및 이용목적이 달성된 후에는 해당 정보를 지체 없이 파기합니다. 파기절차 및 방법은 다음과 같습니다. </p>
		<p>1. 파기절차 <br>
		이용자의 개인정보는 목적이 달성된 후 내부 방침 및 기타 관련 법령에 의한 정보보호 사유에 따라(보유 및 이용기간 참조) 일정 기간 저장된 후 5일 이내에 파기됩니다. 별도 DB로 옮겨진 개인정보가 있는 경우, 법률에 의한 경우가 아니고서는 보유되는 이외의 다른 목적으로 이용되지 않습니다. </p>
		<p>2. 파기방법 <br>
		전자적 파일형태로 저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제합니다. 종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기합니다.</p>
		<p><strong>제 8조 개인정보의 안전성 확보조치</strong></p>
		<p>1. 개인정보 보호를 위한 기술적, 관리적 조치<br>
		본원은 이용자의 개인정보를 취급함에 있어 개인정보가 분실, 도난, 누출, 변조 또는 훼손되지 않도록 안전성 확보를 위하여 다음과 같은 기술적, 관리적 조치를 강구하고 있습니다. </p>
		<p>(1) 내부관리계획의 수립 및 시행<br>
		① 본원은 개인정보의 안전한 처리를 위하여 내부관리계획을 수립하고 시행하고 있습니다.<br>
		② 본원은 원내 개인정보보호 전담기구 등을 통해 개인정보보호조치의 이행사항 및 담당자의 준수여부를 확인하여 문제 발견 시 즉시 시정 조치하도록 하고 있습니다.</p>
		<p>(2) 개인정보의 암호화 <br>
		이용자의 개인정보는 비밀번호에 의해 보호되며, 파일 및 전송 데이터를 암호화하거나 파일 잠금기능(Lock)을 사용하여 저장, 관리되며, 중요한 데이터는 별도의 보안기능을 통해 보호되고 있습니다. </p>
		<p>(3) 개인정보 검색엔진 노출 방지<br>
		구글, 네이버 등 포탈 검색 시 게시판이나 첨부파일에 포함된 개인정보가 누출되지 않도록 게시판 관리하고 있습니다.</p>
		<p>(4) 관리자 페이지 인증절차<br>
		관리자 페이지의 암호의 복잡도를 높임으로써, 유출 가능성을 최소화하고 있습니다.</p>
