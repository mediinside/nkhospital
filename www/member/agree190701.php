	<h2 class="title">개인정보 처리방침 동의</h2>
	<div class="agreeTxtWrap">
		<p>'의료법인 인봉의료재단 뉴고려병원병원'(이하 '본원'이라 합니다)는 수집하는 개인정보를 중요시하며, 「정보통신망 이용촉진 및 정보보호에 관한 법률」, 「개인정보보호법」을 준수하기 위하여 노력하고 있습니다. </p>
		<p>본원은 개인정보취급(처리)방침을 통하여 본원이 홈페이지를 통해 제공받은 개인정보를 어떠한 용도와 방식으로 이용하고 있으며, 개인정보보호를 위해 어떠한 조치를 취해지고 있는지 알려 드립니다. </p>
		<p><strong>제 1조 개인정보 수집에 대한 동의</strong></p>
		<p>본원은 귀하께서 회원 가입 시 개인정보 취급방침 또는 이용약관의 내용을 공지하며 회원가입 버튼을 클릭할 경우 개인정보 수집에 대해 동의하신 것으로 봅니다.</p>
		<p><strong>제 2조 개인정보의 수집 항목 및 수집 방법</strong></p>
		<p>1. 수집하는 개인정보의 항목<br>
		본원이 관리하는 홈페이지를 통해 최초 회원 가입 또는 서비스 이용 시 이용자로부터 아래와 같은 개인정보를 수집하고 있으며, 이용자의 개인정보 수집 시 서비스 제공을 위하여 필요한 범위에서 최소한의 개인정보를 수집합니다. </p>
		<p>(1) 필수항목<br>
		※ 이용자는 필수항목 수집·이용에 대한 동의를 거부하실 수 있지만 이는 서비스 제공에 필수적으로 제공되어야 하는 정보입니다. 따라서 동의를 거부하실 경우 홈페이지 회원 가입, 서비스 이용 등을 할 수 없습니다. </p>
		<p>① 회원가입, 온라인상담, 인터넷 진료예약을 위해 필요한 개인정보<br>
		- 이메일 주소, 전화번호(자택 및 휴대폰), 비밀번호, 성명, 로그인 아이디, 성별, 생년월일</p>
		<p>② 각 서비스 이용을 위해 필요한 정보, 서비스 이용기록 정보<br>
		- 각 서비스 이용 기록 관련 정보 <br>
		- 로그데이터, 쿠키, 이용시간, 이용자가 입력한 검색어 등<br>
		- 기기 정보, 접속IP정보, 환경설정정보 등</p>
		<p>(2) 선택항목<br>
		※ 이용자는 선택항목 수집·이용에 대한 동의를 거부하실 수 있으며,이는 서비스 제공에 필수적으로 제공되어야 하는 정보가 아닙니다. 따라서 동의를 거부하시더라도 홈페이지 이용이 가능합니다. 서비스 이용과정에서 선택항목 수집, 이용이 필요할 경우 별도로 안내하고 동의 받도록 하겠습니다.</p>
		<p>① 채용 게시판 및 기타 행사참여 등과 관련하여 본인이 제공한 정보</p>
		<p>2. 개인정보 수집 제한 항목<br>
		<b>본원은 홈페이지를 통해 이용자의 사생활을 현저히 침해할 우려가 있는 민감정보(사상·신념, 노동조합·정당의 가입·탈퇴, 정치적 견해, 생체정보 등에 관한 정보 등)는 수집하지 않습니다.</b></p>
		<p>3. 14세미만 어린이들에 대한 보호정책<br>
		본원은 원칙적으로 이용자가 만 14세 미만자일 경우 개인정보를 수집하지 않습니다. 단 진료를 위해 개인정보를 수집했을 경우, 개인 정보를 부모의 동의 없이 제3자와 공유하지 않으며 사용자에 관한 정보를 매매하거나 대여하지 않습니다.</p>
		<p>※ 홈페이지에서는 원칙적으로 개인정보 실명인증 절차를 거치지 않습니다. </p>
		<p>2. 개인정보의 수집방법</p>
		<p>(1) 홈페이지를 통한 수집 원칙<br>
		본원은 원칙적으로 홈페이지를 통한 수집만을 하고 있으며, 비회원이 글을 남기는 경우에는 이용자께서 본원의 개인정보 수집·이용 동의서 각각의 내용에 대해 "동의" 또는 "동의하지 않음"을 선택할 수 있는 절차를 마련하고 있습니다.</p>
		<p>(2) 쿠키에 의한 개인정보 수집<br>
		본원은 귀하에 대한 정보를 저장하고 수시로 찾아내는 '쿠키 (cookie)' 를 사용합니다. 귀하가 웹사이트에 접속을 하면 웹서버는 귀하의 브라우저에 있는 쿠키의 내용을 읽고, 귀하의 추가 정보를 귀하의 컴퓨터에서 찾아 접속에 따른 아이디 등의 추가 입력 없이 서비스를 제공할 수 있습니다. 쿠키는 귀하의 컴퓨터는 식별하지만 귀하를 개인적으로 식별하지는 않습니다.</p>
		<p>※ 쿠키는 웹사이트가 귀하의 컴퓨터 브라우저로 전송하는 소량의 정보입니다. 귀하는 쿠키에 대한 선택권이 있습니다. 웹브라우저의 옵션을 조정함으로써 모든 쿠키를 다 받아들이거나, 쿠키가 설치될 때 통지를 보내도록 하거나 아니면 모든 쿠키를 거부할 수 있는 선택권을 가질 수 있습니다.</p>
		<p><strong>제 3조 개인정보의 수집 목적 및 이용 목적</strong></p>
		<p><b>본 원은 귀하께서 본원의 홈페이지를 이용하거나 본원에서 각종 서비스를 제공하기 위한 목적 등을 위해 수집하며 이용 목적이 변경될 시에는 사전 동의를 구할 것입니다.</b></p>
		<p>1. 홈페이지 회원 가입 및 관리</p>
		<p>(1) 회원 가입의사 확인, 회원제 서비스 제공에 따른 본인 식별·인증<br>
		(2) 각종 고지·통지, 고충처리, 분쟁 조정을 위한 기록 보존<br>
		(3) 불량회원의 부정이용 방지와 비인가 사용방지 등</p>
		<p>2. 민원처리 및 고객관리 서비스 제공</p>
		<p>(1) 진단 및 치료 등 진료를 위한 서비스 제공<br>
		(2) 검사 결과 및 개인별 맞춤 SMS, E-mail 정보 제공에 이용<br>
		(3) 새로운 서비스, 새로운 건강 컨텐츠 제공 및 행사정보 등의 안내<br>
		(4) 연구 및 임상시험, 의학 관련 교육 및 실습, 수탁검사 및 진료통계 목적<br>
		(5) 소비자 기본법 제 52조에 의거한 소비자위해 정보의 수집<br>
		(6) 개인정보 처리 및 정보주체의 개인정보 열람, 정정, 삭제, 처리정지 요구 시 본인확인, 개인식별을 위함</p>
		<p><strong>제 4조 개인정보의 제 3자에 대한 제공</strong></p>
		<p>본원은 회원의 개인 정보를 개인 정보의 수집 및 이용목적에서 고지한 범위 내에서만 사용하며, 동 범위를 초과하여 이용하거나 타인 또는 타기업, 기관에 임의로 제공하지 않습니다. 다만 이용자 본인이 허락한 경우이거나, 관계법령에 의거 적법한 절차에 의한 정부기관의 요구가 있을 경우는 예외로 합니다.<br>
